package com.empeligibility.org.model;

public class JobCriteria {
	public int jobId;
	public String jobName;
	public String ElgibilityFormulae;
	public int getJobId() {
		return jobId;
	}
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getElgibilityFormulae() {
		return ElgibilityFormulae;
	}
	public void setElgibilityFormulae(String elgibilityFormulae) {
		ElgibilityFormulae = elgibilityFormulae;
	}
	
}
